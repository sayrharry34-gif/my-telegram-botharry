async def send_spam(client, name):
    while True:
        try:
            msg = random.choice(MESSAGES_LIST)
            await client.send_message(SPAM_TARGET_CHAT, msg)
            print(f"📤 {name} ပို့ပြီးပြီ: {msg}")
            await asyncio.sleep(random.uniform(5, 15)) 
        except Exception as e:
            print(f"❌ {name} Error: {e}")
            await asyncio.sleep(60)

async def main():
    tasks = []
    for s_name in sessions:
        client = TelegramClient(s_name, API_ID, API_HASH)
        await client.start()
        print(f"✅ {s_name} Login အောင်မြင်သည်")
        tasks.append(send_spam(client, s_name))
    
    await asyncio.gather(*tasks)

if __name__ == '__main__':
    asyncio.run(main())
